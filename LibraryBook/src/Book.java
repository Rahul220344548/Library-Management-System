
public class Book {
	
	private int bookID;
	private String title;
	private String author;
	private boolean isIssued;
	
	public Book( int bookID, String title, String author, boolean isIssued ) {
		this.bookID = bookID;
		this.title = title;
		this.author = author;
		this.isIssued = isIssued;
	}
	
	public int getBookID() {
		return bookID;
	}
	
	
	
	
	
	
}
